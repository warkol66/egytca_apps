	/**
	 * Selects a collection of GraphRelation objects pre-filled with their Actor objects.
	 * @param      Criteria  $criteria
	 * @param      PropelPDO $con
	 * @param      String    $join_behavior the type of joins to use, defaults to Criteria::LEFT_JOIN
	 * @return     array Array of GraphRelation objects.
	 * @throws     PropelException Any exceptions caught during processing will be
	 *		 rethrown wrapped into a PropelException.
	 */
	public static function doSelectJoinActorRelatedByActor1id(Criteria $criteria, $con = null, $join_behavior = Criteria::LEFT_JOIN)
	{
		$criteria = clone $criteria;

		// Set the correct dbName if it has not been overridden
		if ($criteria->getDbName() == Propel::getDefaultDB()) {
			$criteria->setDbName(self::DATABASE_NAME);
		}

		GraphRelationPeer::addSelectColumns($criteria);
		$startcol = GraphRelationPeer::NUM_HYDRATE_COLUMNS;
		ActorPeer::addSelectColumns($criteria);

		$criteria->addJoin(GraphRelationPeer::ACTOR1ID, ActorPeer::ID, $join_behavior);

		$stmt = BasePeer::doSelect($criteria, $con);
		$results = array();

		while ($row = $stmt->fetch(PDO::FETCH_NUM)) {
			$key1 = GraphRelationPeer::getPrimaryKeyHashFromRow($row, 0);
			if (null !== ($obj1 = GraphRelationPeer::getInstanceFromPool($key1))) {
				// We no longer rehydrate the object, since this can cause data loss.
				// See http://www.propelorm.org/ticket/509
				// $obj1->hydrate($row, 0, true); // rehydrate
			} else {

				$cls = GraphRelationPeer::getOMClass(false);

				$obj1 = new $cls();
				$obj1->hydrate($row);
				GraphRelationPeer::addInstanceToPool($obj1, $key1);
			} // if $obj1 already loaded

			$key2 = ActorPeer::getPrimaryKeyHashFromRow($row, $startcol);
			if ($key2 !== null) {
				$obj2 = ActorPeer::getInstanceFromPool($key2);
				if (!$obj2) {

					$cls = ActorPeer::getOMClass(false);

					$obj2 = new $cls();
					$obj2->hydrate($row, $startcol);
					ActorPeer::addInstanceToPool($obj2, $key2);
				} // if obj2 already loaded

				// Add the $obj1 (GraphRelation) to $obj2 (Actor)
				$obj2->addGraphRelationRelatedByActor1id($obj1);

			} // if joined row was not null

			$results[] = $obj1;
		}
		$stmt->closeCursor();
		return $results;
	}


